void solve(){
    int n, a, b;
    cin >> n >> a >> b;

    int burles = 0;
    while (n > 0){
        if (n > 1){
            burles += min(2*a, b);
            n-=2;
        }
        else {
            burles += a;
            n--;
        }
    }
    cout << burles << '\n';
}

