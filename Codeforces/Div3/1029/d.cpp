ll binpow(ll a, ll b) {
    ll res = 1;
    while (b > 0) {
        if (b % 2){
            res *= a;
#ifdef MOD_ANSWER
            res %= MOD;
#endif
        } 
        a *= a;
#ifdef MOD_ANSWER
        a %= MOD;
#endif
        b /= 2;
    }
    return res;
}

#define ONLINE_JUDGE
void solve(){
#ifndef ONLINE_JUDGE
    freopen("input.txt","r",stdin);
    freopen("output.txt","w",stdout);
#endif
    int n;
    cin >> n;
    vector<ll> arr(n);
    for (ll& i : arr) cin >> i;

    // let x be no. of 1st operation
    // let y be no. of 2nd operation
    ll numy = (ll)n * arr[0] - arr[n-1];
    ll denom = (ll)n * n - 1;

    if (numy % denom != 0) {
        cout << "NO\n";
        return;
    }

    ll y = numy / denom;
    ll x = arr[0] - n * y;
    if (x < 0 || y < 0) {
        cout << "NO\n";
        return;
    }

    for (int i = 0; i < n; i++) {
        ll val = x * (i+1) + y * (n - i);
        if (arr[i] != val) {
            cout << "NO\n";
            return;
        }
    }

    cout << "YES\n";
}

