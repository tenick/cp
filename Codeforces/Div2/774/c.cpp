// use pos = 0, if you want to work with 1st bit of given number
template <typename T> inline bool bit_check(T number, int pos) {
    return (number >> pos) & (T)1;
}
template <typename T> inline void bit_toggle(T& number, int pos) {
    number ^= ((T)1 << pos);
}
template <typename T> inline T bit_clear(T number, int pos) {
    return number & ~((T)1 << pos);
}
template <typename T> inline void bit_set(T& number, int pos) {
    number |= ((T)1 << pos);
}

int minans = 1e9;
ll factorials[] {1, 1, 2, 6, 24, 120, 720, 5040, 40320, 362880, 3628800, 39916800, 479001600, 6227020800, 87178291200, 1307674368000, 20922789888000};
void rc(int i, int took, ll curr) {
    if (i > 16) 
        return;
    if (curr - factorials[i] < 0)
        return;
    
    // take
    ll newcurr = curr - factorials[i];
    minans = min(minans, took + 1 + __builtin_popcountll(newcurr));
    rc(i+1, took+1, newcurr);

    // skip
    rc(i+1, took, curr);
}

void solve(){
    ll n;
    cin >> n;
    minans = __builtin_popcountll(n);
    rc(0, 0, n);
    cout << minans << '\n';
}

